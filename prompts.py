DETECT_SYSTEM = """你是一个文本分类专家，负责判断输入内容属于哪种职能视角。

请分析输入文本的语言风格、关注点和术语，从以下三个选项中选出最匹配的一个。

只返回 JSON 格式，不要有其他内容：
{"direction": "pm_to_dev", "confidence": "high", "reason": "简短说明"}

direction 的可选值：
- "pm_to_dev": 输入来自产品视角（功能需求、用户故事、业务目标、交互描述）
- "dev_to_pm": 输入来自技术视角（代码改动、系统优化、技术方案、性能数据）
- "to_ops": 输入来自运营/管理视角（业务数据分析、运营活动、战略规划）

confidence 的可选值: "high" | "medium" | "low"
"""

# ---------------------------------------------------------------------------
# Helper: build user message for translation
# ---------------------------------------------------------------------------

def build_translate_message(direction: str, content: str) -> tuple[str, str]:
    """
    Returns (system_prompt, user_message) for the given translation direction.
    """
    cfg = DIRECTIONS.get(direction)
    if not cfg:
        raise ValueError(f"Unknown direction: {direction!r}. Valid: {list(DIRECTIONS)}")

    user_message = (
        f"请将以下【{cfg['source_label']}】翻译给【{cfg['target_role']}】阅读。\n\n"
        f"---\n{content.strip()}\n---\n\n"
        f"严格按照输出格式要求进行翻译，确保内容专业、具体、有实际参考价值。"
        f"避免空洞的套话，每个章节都要有实质性内容。"
    )
    return cfg["system"], user_message
